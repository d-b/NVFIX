#include "NVFIX.hpp"

static HMODULE _handle_api = NULL;
static PVOID(*_proc_queryinterface)(NvU32 offset) = NULL;

template<typename T, typename... Ts>
auto invoke(NvU32 offset, T func, Ts&&... args) -> typename std::result_of<T&(Ts...)>::type {
    return ((T)_proc_queryinterface(offset))(std::forward<Ts>(args)...);
}

NvAPI_Status NvAPI_Private_Init() {
    // Obtain the handle for the NVAPI library
#ifdef _WIN64
    _handle_api = LoadLibrary(L"nvapi64.dll");
#else
    _handle_api = LoadLibrary(L"nvapi.dll");
#endif
    if (!_handle_api) return NVAPI_ERROR;

    // Obtain the query interface procedure
    _proc_queryinterface = (decltype(_proc_queryinterface))GetProcAddress(_handle_api, "nvapi_QueryInterface");

    // Check result
    return (_proc_queryinterface != NULL) ? NVAPI_OK : NVAPI_ERROR;
}

NvAPI_Status NvAPI_Private_Unload() {
    if (_handle_api) FreeLibrary(_handle_api);
    return NVAPI_OK;
}

